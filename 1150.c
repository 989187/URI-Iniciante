#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <math.h>

int main(){
	
    int a, b, c=0, x, z;
    
    scanf("%d", &x);
    
    while(x >= z){
    	scanf("%d", &z);
	}
	
    for(a = x, b = 0; b < z; a ++){
        b += a;
        c ++;
    }
    printf("%d\n", c);
    
    return 0;
}
